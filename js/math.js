const doAdd = (a, b) => {
  return a + b;
};

const doSubstract = (a, b) => {
  return a - b;
};

const doMultiplay = (a, b) => {
  return a * b;
};

const doDivide = (a, b) => {
  return a / b;
};

const doPercent = (a, b) => {
  return a * (b / 100);
};

const addPercent = (a, b) => {
  return a + a * (b / 100);
};

const substractPercent = (a, b) => {
  return a - a * (b / 100);
};
